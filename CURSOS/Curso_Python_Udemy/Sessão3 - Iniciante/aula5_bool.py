# Tipo de dados boll (boleano)
# Ao qeustionar algo em programa, só existem duas respostas possiveis:
# Sim(True) e Não(False)
# Exitem vários operadores par "Questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro
print(10 == 10) #Sim => True (Verdadeiro)
print(10 == 11) #Não => False (Falso)
print(type(10 == 11))
print(10 == "10")