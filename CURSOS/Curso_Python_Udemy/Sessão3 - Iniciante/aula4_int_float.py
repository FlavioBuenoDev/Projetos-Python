# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo
print(11) #int
print(-11) #int
print(0) #int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número positivo ou negativo com punto flutuante
# float sem sinal é considerado positivo
print(1.1, 10.11)

# A funçaõ type mostra o type que o Python inferiu ao valor
print(type(4))
print(type("Flavio"))
print(type(1.1), type(-1.1), type(0.0))